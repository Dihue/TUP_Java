package Cuatrimestre_01.Clases;

public class C04_Iteracion {

    // Ejemplos de iteradores

    public static void main(String[] args) {

        // Iteracion FOR
        for (int i = 0; i < 10; i++) {
            // Accion 1
            // Accion 2
        }

        // Iteracion WHILE
        boolean condicion = true;

        while (condicion) {
            // Accion 1
            // Accion 2
        }

        // Iteracion DO WHILE
        boolean condicion_2 = true;

        do {
            // Accion 1
            // Accion 2
        } while (condicion_2);

    }
}
