package Cuatrimestre_01.Clases;

public class C01_Variables {
    /*
    Constante: si se ubica fuera del main, se debe usar
    la palabra reservada "static"
     */
    static final int constante = 1;

    public static void main(String[] args) {

        // Constante: se usa la palabra reservada "final"
        final double PI = 3.1416;

        // Numeros enteros
        int numero = 1234567890;
        short numero_corto = 12345;

        // Numeros decimales
        double decimal_1 = 1.1234567890;
        float decimal_2 = 1.12f;

        // Caracter
        char caracter = 'a';

        // Cadena de caracteres
        String cadena = "Hola Mundo";

        // Boolean
        boolean a = true;
        boolean b = false;

    }
}
